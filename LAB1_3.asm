.EQU	OUTPUT_DDR	=	DDRA
.EQU	OUTPUT_PORT	=	PORTA
.EQU	OUTPUT_PIN	=	PINA

.EQU	INPUT_DDR	=	DDRB
.EQU	INPUT_PORT	=	PORTB
.EQU	INPUT_PIN	=	PINB


LDI		R16,			0X00
OUT		INPUT_DDR,		R16
LDI		R16,			0XFF
OUT		INPUT_PORT,		R16

LDI		R16,			0XFF
OUT		OUTPUT_DDR,		R16

LOOP: 
IN		R16,			INPUT_PIN	
COM		R16

MOV		R17,			R16
ANDI	R16,			$F0		;R16 = 4 BIT CAO
SWAP	R16

ANDI	R17,			$0F		;R17 = 4 BIT THAP

MUL		R16,			R17		;R1:R0 = R16*R17
OUT		OUTPUT_PORT,	R0 

RJMP	LOOP
