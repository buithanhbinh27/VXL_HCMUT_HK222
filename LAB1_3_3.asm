HEX_ASC:
		CPI			R17,			0X0A ;KÝ T? <$0A?
		BRCS		NUM						;C=1 NH? H?N
		LDI			R18,			0X37 ;KÝ T? CH?
		RJMP		CHAR
NUM: 
		LDI			R18,			0X30 ;KÝ T? S?
CHAR: 
		ADD			R18,			R17 ;C?NG THÊM $30
		RET	
